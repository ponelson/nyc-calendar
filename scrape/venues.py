"""
Venue registry.

Every venue declares HOW its listings can be read, not just where they live.
Strategies, in order of reliability:

  jsonld   - page embeds schema.org Event objects in <script type="application/ld+json">.
             Self-describing, survives redesigns. Best case.
  ics      - venue publishes an iCalendar feed. Even better than jsonld when it exists.
  rss      - RSS/Atom feed with dates in the items.
  custom   - a hand-written adapter in scrape/adapters/. Fragile; use only when the
             venue is worth the maintenance.
  link     - no machine-readable listings. We do not scrape; the calendar links out.

`enabled: False` means "registered but not yet proven". Run probe.py to find out which
of the jsonld/ics guesses actually pay off, then flip them on. Do not trust this file's
guesses — trust the probe.
"""

